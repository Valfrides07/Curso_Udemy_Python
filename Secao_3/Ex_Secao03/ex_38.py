# Comparação entre valores utilizando if

primeiro_valor = input('Digite o valor: ')
segundo_valor = input('Digite o segundo valor: ')

if primeiro_valor > segundo_valor:
    print('O primeiro valor é maior que o segundo valor')

elif primeiro_valor == segundo_valor:
    print('Ambos valores digitados são identicos.')
    
else:
    print('O Segundo valor é maior que o primeiro valor') 